import { serve } from 'https://deno.land/std@0.168.0/http/server.ts'
import { createClient } from 'https://esm.sh/@supabase/supabase-js@2'

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    console.log('üöÄ auth-by-qr-token Edge Function called')
    console.log('üìã Request method:', req.method)
    console.log('üìã Request URL:', req.url)
    
    const supabaseUrl = Deno.env.get('SUPABASE_URL')!
    const supabaseServiceKey = Deno.env.get('SUPABASE_SERVICE_ROLE_KEY')!
    
    if (!supabaseUrl || !supabaseServiceKey) {
      console.error('‚ùå Missing environment variables')
      throw new Error('Missing Supabase configuration')
    }
    
    const supabaseAdmin = createClient(supabaseUrl, supabaseServiceKey, {
      auth: {
        autoRefreshToken: false,
        persistSession: false
      }
    })

    // –ü–æ–ª—É—á–∞–µ–º —Ç–æ–∫–µ–Ω –∏–∑ body (POST) –∏–ª–∏ URL path (GET)
    let token: string;
    
    if (req.method === 'POST') {
      const body = await req.json()
      token = body.token
    } else {
      // GET –∑–∞–ø—Ä–æ—Å - —Ç–æ–∫–µ–Ω –∏–∑ URL path
      const url = new URL(req.url)
      const pathParts = url.pathname.split('/')
      token = pathParts[pathParts.length - 1] // –ø–æ—Å–ª–µ–¥–Ω—è—è —á–∞—Å—Ç—å –ø—É—Ç–∏
    }
    
    if (!token || token === 'auth-by-qr-token') {
      console.error('‚ùå No token provided. URL:', req.url)
      throw new Error('Token is required')
    }

    console.log('üîç Looking up QR token:', token.substring(0, 8) + '...')

    // –ù–∞—Ö–æ–¥–∏–º –∞–∫—Ç–∏–≤–Ω—ã–π —Ç–æ–∫–µ–Ω
    const { data: qrToken, error: tokenError } = await supabaseAdmin
      .from('user_qr_tokens')
      .select('user_id')
      .eq('token', token)
      .eq('is_active', true)
      .single()

    if (tokenError || !qrToken) {
      console.error('‚ùå Invalid or expired token:', tokenError)
      throw new Error('Invalid or expired QR token')
    }

    console.log('‚úÖ Valid token found for user:', qrToken.user_id)

    // –ü–æ–ª—É—á–∞–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    const { data: usersList, error: usersError } = await supabaseAdmin.auth.admin.listUsers()
    if (usersError) {
      console.error('‚ùå Error listing users:', usersError)
      throw new Error('Failed to list users')
    }

    const user = usersList.users.find(u => u.id === qrToken.user_id)
    if (!user) {
      throw new Error('User not found')
    }

    console.log('‚úÖ User found:', user.email)

    // –°–æ–∑–¥–∞–µ–º —Å–µ—Å—Å–∏—é –Ω–∞–ø—Ä—è–º—É—é –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    const { data: sessionData, error: sessionError } = await supabaseAdmin.auth.admin.generateLink({
      type: 'magiclink',
      email: user.email!,
      options: {
        redirectTo: 'https://paneldoirp.vercel.app/auth/qr/success'
      }
    })

    if (sessionError) {
      console.error('‚ùå Error generating session:', sessionError)
      throw new Error(`Failed to generate session: ${sessionError.message}`)
    }

    console.log('‚úÖ Session generated for:', user.email)

    // –ò–∑–≤–ª–µ–∫–∞–µ–º —Ç–æ–∫–µ–Ω—ã –∏–∑ magic link
    const magicLinkUrl = sessionData.properties?.action_link
    if (!magicLinkUrl) {
      throw new Error('No magic link generated')
    }

    // –ü–∞—Ä—Å–∏–º URL –¥–ª—è –∏–∑–≤–ª–µ—á–µ–Ω–∏—è —Ç–æ–∫–µ–Ω–æ–≤
    const url = new URL(magicLinkUrl)
    const accessToken = url.searchParams.get('access_token') || url.hash.match(/access_token=([^&]+)/)?.[1]
    const refreshToken = url.searchParams.get('refresh_token') || url.hash.match(/refresh_token=([^&]+)/)?.[1]

    if (!accessToken || !refreshToken) {
      // –ï—Å–ª–∏ —Ç–æ–∫–µ–Ω–æ–≤ –Ω–µ—Ç –≤ URL, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º magic link –¥–ª—è –∞–∫—Ç–∏–≤–∞—Ü–∏–∏
      return new Response(JSON.stringify({
        success: true,
        redirectUrl: magicLinkUrl,
        needsActivation: true
      }), {
        status: 200,
        headers: {
          ...corsHeaders,
          'Content-Type': 'application/json'
        }
      })
    }

    // –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ç–æ–∫–µ–Ω—ã –Ω–∞–ø—Ä—è–º—É—é
    return new Response(JSON.stringify({
      success: true,
      accessToken,
      refreshToken,
      user: {
        id: user.id,
        email: user.email,
        user_metadata: user.user_metadata
      }
    }), {
      status: 200,
      headers: {
        ...corsHeaders,
        'Content-Type': 'application/json'
      }
    })

  } catch (error) {
    console.error('‚ùå Error in auth-by-qr-token:', error)
    
    // –í–æ–∑–≤—Ä–∞—â–∞–µ–º –æ—à–∏–±–∫—É –≤ JSON
    return new Response(JSON.stringify({
      success: false,
      error: error.message || 'Authentication failed'
    }), {
      status: 400,
      headers: {
        ...corsHeaders,
        'Content-Type': 'application/json'
      }
    })
  }
})
